using System;

namespace Enemies
{
    /// <summary>
    /// Represents a zombie enemy.
    /// </summary>
    public class Zombie
    {
        /// <summary>
        /// Represents health of the zombie.
        /// </summary>
        public int health;

        /// <summary>
        /// Initializes new instance of <see cref="Zombie"/> class.
        /// </summary>
        public Zombie()
        {
            health = 0;
        }
    }
}